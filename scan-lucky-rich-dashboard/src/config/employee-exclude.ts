function normalizeName(s: string): string {
  return (s || '')
    .replace(/\s+/g, ' ')
    .trim()
    .replace(/^(นางสาว|นาง|นาย|น\.ส\.|ด\.ช\.|ด\.ญ\.|ว่าที่ร\.ต\.)\s*/, '')
    .trim()
}

/** เบอร์ → 9 หลักท้าย (กันเบอร์ขาด 0 นำ / มีขีด) */
function last9(phone: string | null | undefined): string {
  const d = (phone || '').replace(/\D/g, '')
  return d.length >= 9 ? d.slice(-9) : ''
}

const NORMALIZED_TO_ORIGINAL = new Map(EMPLOYEE_EXCLUDE_NAMES.map((n) => [normalizeName(n), n] as const))
const PHONE_TO_NAME = new Map<string, string>(EMPLOYEE_EXCLUDE_PHONE_NAME)

/** คืนชื่อพนักงาน (canonical) ถ้า match ด้วยชื่อ — ไม่ match คืน null */
export function matchExcludedName(name: string | null | undefined): string | null {
  if (!name) return null
  return NORMALIZED_TO_ORIGINAL.get(normalizeName(name)) ?? null
}

export function isExcludedScannerName(name: string | null | undefined): boolean {
  return matchExcludedName(name) !== null
}

/** match ด้วย "เบอร์ หรือ ชื่อ" → คืนชื่อพนักงาน (canonical) ถ้าเป็นพนักงาน · ไม่ใช่คืน null */
export function matchExcluded(name: string | null | undefined, phone: string | null | undefined): string | null {
  const byName = matchExcludedName(name)
  if (byName) return byName
  const byPhone = PHONE_TO_NAME.get(last9(phone))
  return byPhone ?? null
}
